    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        regusuarioBean oregusuarioBean = new regusuarioBean();
        SeguridadApp segApp = new SeguridadApp();
        try {
            if (!txtPassword.getText().equals(txtConfPassword.getText())) {
                JOptionPane.showInternalMessageDialog(this, "LAS CONTRASEÑAS NO COINCIDEN", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            oregusuarioBean.setNvNombUsuario(txtUsuario.getText());
            oregusuarioBean.setCCodRegTrabajador(codigo.getText());
            oregusuarioBean.setNvContrasenia(segApp.EncriptarApp(txtPassword.getText()));

            oregusuarioBean.setIIdRegUsuaRegistra(frminicio.IdUsuario);
            oregusuarioBean.setIOps(1);

            if (nuevo) {
                oregusuarioBean.setNvObservacion(" Nuevo Registro");
                boolean result = Generic.getInstance(regusuarioBL.class).fnInsertarregusuarioBean(oregusuarioBean);
                if (result) {
                    JOptionPane.showInternalMessageDialog(this, "Registro correcto del Usuario", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showInternalMessageDialog(this, "Fallo al registrar el Usuario", "Mensaje", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                oregusuarioBean.setNvObservacion("Modificado ");
                int index = jTable1.getSelectedRow();
                String dat = modeloTabla.getValueAt(index, 0).toString();
                oregusuarioBean.setIIdRegUsuario(Integer.parseInt(dat));

                boolean result = Generic.getInstance(regusuarioBL.class).fnActualizarregusuarioBean(oregusuarioBean);
                if (result) {
                    JOptionPane.showInternalMessageDialog(this, "Actualizacion correcta del Usuario", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showInternalMessageDialog(this, "Fallo al Actualizar el Usuario", "Mensaje", JOptionPane.ERROR_MESSAGE);
                }
            }
            activarBotones(true, false, false, false, true);
            activarCampos(true, false);
            limpiarCampos();
            txtBuscarUsuario.requestFocus();
            txtBuscarUsuario.selectAll();
            iniciarGrilla(6);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


