    private void cargarCombo() {
        try {
            desde.removeAllItems();
            hasta.removeAllItems();
            vencimientoBean ovencimientoBean = new vencimientoBean();
            ovencimientoBean.setIOps(2);
            ArrayList<vencimientoBean> lista = Generic.getInstance(vencimientoBL.class).fnAutocompletevencimientoBean(ovencimientoBean);

            for (vencimientoBean v : lista) {
                desde.addItem(v);
                hasta.addItem(v);
                mapaDesde.put(v.getIIdVencimiento(), v);
                mapaHasta.put(v.getIIdVencimiento(), v);
            }
        } catch (Exception ex) {
            Logger.getLogger(frmgenerarDeudaIndividual.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void limpiarControles() {
        observacion.setText("");
        dni.setText("");
        desde.setSelectedIndex(0);
        hasta.setSelectedIndex(0);
        actual.setSelected(true);
        desde.setEnabled(false);
        hasta.setEnabled(false);
    }
