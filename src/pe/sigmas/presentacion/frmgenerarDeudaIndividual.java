    private void individualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_individualActionPerformed
        // TODO add your handling code here:
        regobligacionBean oregobligacionBean = new regobligacionBean();
        try {
            if (!dni.getText().isEmpty() && !observacion.getText().isEmpty() && dni.getText().length() >= 8) {
                regcontribuyenteBean oregcontribuyenteBean = new regcontribuyenteBean();
                oregcontribuyenteBean.setIOps(3);
                  if (!validator.validarInteger(dni.getText())) {
                    JOptionPane.showInternalMessageDialog(this, "Numero de Documento Inocrrecto", "ERROR", JOptionPane.ERROR_MESSAGE);
                    dni.selectAll();
                    dni.requestFocus();
                    return;
                }
                oregcontribuyenteBean.setCCodRegContribuyente(dni.getText());
                oregcontribuyenteBean = Generic.getInstance(regcontribuyenteBL.class).fnGetregcontribuyenteBean(oregcontribuyenteBean);

                if (oregcontribuyenteBean != null) {
                    if (actual.isSelected()) {
                        oregobligacionBean.setIOps(2);
                    } else {
                        oregobligacionBean.setIOps(3);
                        int id1 = ((vencimientoBean) desde.getSelectedItem()).getIIdVencimiento();
                        int id2 = ((vencimientoBean) hasta.getSelectedItem()).getIIdVencimiento();
                        if (id1 >= id2) {
                            JOptionPane.showInternalMessageDialog(this, "Vencimiento Incorrectamente Seleccionado ", "Error", JOptionPane.WARNING_MESSAGE);
                            return;
                        }
                        oregobligacionBean.setBiIdRegObligacion(id1);
                        oregobligacionBean.setIIdVencimiento(id2);
                    }
                    oregobligacionBean.setCCodRegContribuyente(dni.getText());
                    oregobligacionBean.setNvObservacion(observacion.getText());
                    oregobligacionBean.setIIdRegUsuaRegistra(frminicio.IdUsuario);

                    boolean result = Generic.getInstance(regobligacionBL.class).fnInsertarregobligacionBean(oregobligacionBean);
                    if (result) {
                        limpiarControles();
                        JOptionPane.showInternalMessageDialog(this, "Generacion de deuda individual correcta.", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showInternalMessageDialog(this, "Ocurrio error al generar deuda individual", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showInternalMessageDialog(this, "El Contribuyente con el numero de DNI :" + dni.getText() + " no Existe.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showInternalMessageDialog(this, "Ingrese los datos necesarios para proceder con el calculo", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            System.out.println("ocurrio un error");
        }
    }//GEN-LAST:event_individualActionPerformed

