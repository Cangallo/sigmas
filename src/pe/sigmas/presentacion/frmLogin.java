    private void btningresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btningresarActionPerformed
        try {
            regusuarioBean oregusuarioBean = new regusuarioBean();
            SeguridadApp segApp = new SeguridadApp();
            if (txtusuario.getText().isEmpty() || txtpassword.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "INGRESE LOS DATOS NECESARIOS PARA CONTINUAR", "Acceso al Sistema", JOptionPane.ERROR_MESSAGE);
            } else {
                oregusuarioBean.setNvNombUsuario(txtusuario.getText());
                oregusuarioBean.setNvContrasenia(segApp.EncriptarApp(txtpassword.getText()));
                oregusuarioBean.setIOps(1);
                oregusuarioBean = Generic.getInstance(regusuarioBL.class).fnGetregusuarioBean(oregusuarioBean);
                if (oregusuarioBean != null) {
                    frminicio.loginCorrecto(oregusuarioBean.getIIdRegUsuario(), "USUARIO ACTUAL : " + oregusuarioBean.getNvNombUsuario());
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "INGRESE CORRECTAMENTE SU USUARIO Y CONTRASEÃ‘A", "Acceso al Sistema", JOptionPane.ERROR_MESSAGE);
                }

            }

        } catch (Exception e) {
        }

    }//GEN-LAST:event_btningresarActionPerformed

