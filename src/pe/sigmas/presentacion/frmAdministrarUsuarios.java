    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (modeloTabla.getRowCount() > 0) {
            int index = jTable1.getSelectedRow();
            if (index > -1) {
                try {
                    int i = JOptionPane.showInternalConfirmDialog(this, "ESTA SEGURO DE ELIMINAR EL USUARIO", "MENSAJE", JOptionPane.OK_CANCEL_OPTION);
                    String dat = modeloTabla.getValueAt(index, 0).toString();
                    regpersonaBean oregpersonaBean = new regpersonaBean();
                    oregpersonaBean.setIIdRegUsuaRegistra(frminicio.IdUsuario);
                    oregpersonaBean.setIOps(2);
                    if (i == 0) {
                        oregpersonaBean.setBiIdRegPersona(Integer.parseInt(dat));
                        boolean result = Generic.getInstance(regpersonaBL.class).fnActualizarregpersonaBean(oregpersonaBean);
                        if (result) {
                            iniciarGrilla(6);
                            limpiarCampos();
                            activarBotones(true, true, true, false, false);
                            JOptionPane.showInternalMessageDialog(this, "Eliminacion correcta del Usuario", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                        } else {
                            JOptionPane.showInternalMessageDialog(this, "Fallo al Eliminar el Usuario", "Mensaje", JOptionPane.ERROR_MESSAGE);
                        }
                    }
                } catch (Exception ex) {
                    Logger.getLogger(frmAdministrarUsuarios.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

