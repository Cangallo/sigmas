    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        regpersonaBean oregpersonaBean = new regpersonaBean();
        try {
            //validar el ingreso de datos 

            oregpersonaBean.setCCodMaeSexo(((maesexoBean) sexo.getSelectedItem()).getCCodMaeSexo());
            oregpersonaBean.setCCodMaeTipoPersona(((maetipopersonaBean) tipoPersona.getSelectedItem()).getCCodMaeTipoPersona());
            oregpersonaBean.setCCodMaeEstaCivil(((maetipodocumentoBean) tipoDocumento.getSelectedItem()).getCCodMaeTipoDocumento());
            oregpersonaBean.setNvEmail(((maecategoriaBean) categoria.getSelectedItem()).getCCodMaeCategoria());
            oregpersonaBean.setNvNumDocumento(NumDocumento.getText());
            oregpersonaBean.setNvRpm(CodigoUsuario.getText());
            oregpersonaBean.setNvNombre(nombre.getText());
            oregpersonaBean.setNvApePaterno(apepaterno.getText());
            oregpersonaBean.setNvApeMaterno(apematerno.getText());
            oregpersonaBean.setNvDomiProcesal(direccion.getText());
            oregpersonaBean.setNvObservacion(observacion.getText());
            oregpersonaBean.setIIdRegUsuaRegistra(frminicio.IdUsuario);
            oregpersonaBean.setIOps(1);

            if (nuevo) {
                boolean result = Generic.getInstance(regpersonaBL.class).fnInsertarregpersonaBean(oregpersonaBean);
                if (result) {
                    JOptionPane.showInternalMessageDialog(this, "Registro correcto del Usuario", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showInternalMessageDialog(this, "Fallo al registrar el Usuario", "Mensaje", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                int index = jTable1.getSelectedRow();
                String dat = modeloTabla.getValueAt(index, 0).toString();
                oregpersonaBean.setBiIdRegPersona(Integer.parseInt(dat));

                boolean result = Generic.getInstance(regpersonaBL.class).fnActualizarregpersonaBean(oregpersonaBean);
                if (result) {
                    JOptionPane.showInternalMessageDialog(this, "Actualizacion correcta del Usuario", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showInternalMessageDialog(this, "Fallo al Actualizar el Usuario", "Mensaje", JOptionPane.ERROR_MESSAGE);
                }
            }
            activarBotones(true, false, false, false, true);
            activarCampos(true, false);
            limpiarCampos();
            busquedanombre.requestFocus();
            busquedanombre.selectAll();
            iniciarGrilla(6);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

