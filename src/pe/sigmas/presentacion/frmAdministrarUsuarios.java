    private void cargarCombo() throws Exception {
        tipoPersona.removeAllItems();
        sexo.removeAllItems();
        tipoDocumento.removeAllItems();
        categoria.removeAllItems();
        maetipopersonaBean omaetipopersonaBean = new maetipopersonaBean();
        maesexoBean omaesexoBean = new maesexoBean();
        maetipodocumentoBean omaetipodocumentoBean = new maetipodocumentoBean();
        maecategoriaBean omaecategoriaBean = new maecategoriaBean();
        omaetipopersonaBean.setIOps(2);
        omaesexoBean.setIOps(2);
        omaetipodocumentoBean.setIOps(2);
        omaecategoriaBean.setIOps(2);
        ArrayList<maetipopersonaBean> lista1 = Generic.getInstance(maetipopersonaBL.class).fnAutocompletemaetipopersonaBean(omaetipopersonaBean);
        ArrayList<maesexoBean> lista2 = Generic.getInstance(maesexoBL.class).fnAutocompletemaesexoBean(omaesexoBean);
        ArrayList<maetipodocumentoBean> lista3 = Generic.getInstance(maetipodocumentoBL.class).fnAutocompletemaetipodocumentoBean(omaetipodocumentoBean);
        ArrayList<maecategoriaBean> lista4 = Generic.getInstance(maecategoriaBL.class).fnAutocompletemaecategoriaBean(omaecategoriaBean);

        lista1.stream().map((v) -> {
            tipoPersona.addItem(v);
            return v;
        }).forEach((v) -> {
            mapaPersona.put(v.getCCodMaeTipoPersona(), v);
        });
        lista2.stream().map((v) -> {
            sexo.addItem(v);
            return v;
        }).forEach((v) -> {
            mapaSexo.put(v.getCCodMaeSexo(), v);
        });
        lista3.stream().map((v) -> {
            tipoDocumento.addItem(v);
            return v;
        }).forEach((v) -> {
            mapaDocumento.put(v.getCCodMaeTipoDocumento(), v);
        });
        lista4.stream().map((v) -> {
            categoria.addItem(v);
            return v;
        }).forEach((v) -> {
            mapaCategoria.put(v.getCCodMaeCategoria(), v);
        });
    }
