    private void iniciarGrilla(int i) throws Exception {
        deleteRows();
        regcontribuyenteBean oregcontribuyenteBean = new regcontribuyenteBean();
        oregcontribuyenteBean.setIOps(i);
        oregcontribuyenteBean.setBEstado(true);
        oregcontribuyenteBean.setNvObservacion(busquedanombre.getText());
        List lista = Generic.getInstance(regcontribuyenteBL.class).fnGridregcontribuyenteBean(oregcontribuyenteBean);

        lista.stream().forEach((list) -> {
            modeloTabla.addRow((Object[]) list);
        });
    }

    private void estiloTabla() {
        for (int i = 0; i < modeloColumna.getColumnCount(); i++) {
            jTable1.getColumnModel().getColumn(i).setHeaderRenderer(new GenericEstiloTabla());
        }
    }

    private void hideColumnTable() {
        jTable1.getColumnModel().getColumn(0).setMaxWidth(0);
        jTable1.getColumnModel().getColumn(0).setMinWidth(0);
        jTable1.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(0);
        jTable1.getTableHeader().getColumnModel().getColumn(0).setMinWidth(0);
    }

    private void ajustarColumna() {
        modeloColumna.getColumn(0).setPreferredWidth(0);
        modeloColumna.getColumn(1).setPreferredWidth(50);
        modeloColumna.getColumn(2).setPreferredWidth(50);
        modeloColumna.getColumn(3).setPreferredWidth(200);
        modeloColumna.getColumn(4).setPreferredWidth(20);
        modeloColumna.getColumn(5).setPreferredWidth(10);
    }

    private void deleteRows() {
        while (modeloTabla.getRowCount() > 0) {
            modeloTabla.removeRow(0);
        }
    }
