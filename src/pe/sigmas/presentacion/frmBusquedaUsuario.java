    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:

        if (evt.getClickCount() == 2) {

            if (modeloTabla.getRowCount() > 0) {
                int index = jTable1.getSelectedRow();
                if (index > -1) {
                    try {
                        String idPersona=modeloTabla.getValueAt(index, 0).toString();
                        String codigo = modeloTabla.getValueAt(index, 1).toString();
                        String nombre = modeloTabla.getValueAt(index, 3).toString();
                        frmAdministrarValConcepto.cargarUsuario(idPersona,codigo, nombre);
                        this.dispose();
                    } catch (Exception ex) {
                        Logger.getLogger(frmAdministrarUsuarios.class.getName()).log(Level.SEVERE, null, ex);
                    }

                }

            }
            // do some action
        }


    }//GEN-LAST:event_jTable1MouseClicked

